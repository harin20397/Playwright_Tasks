// If-else statement example
const flag = true;

if (!flag)
{
    console.log("Conditions is satisfied");
}
else
{
    console.log(flag);
    console.log("Conditions is not satisfied");
}

// While loop example
let i = 0;
while (i > 10)
{
    i++;
    console.log(i);
}

// Do-while loop example
do
{
    i++;
} while (i < 15);
console.log(i);

// For loop example
for(let j = 0; j < 5; j++)
{
    console.log(j);
}
